let n1, n2;

n1 = prompt(`Qual o valor do primeiro numero?`);

n2 = prompt(`Qual o valor do segundo valor?`);

if (n1 > n2) {
  document.write(`O  primeiro numero é o maior`);
} else if (n2 > n1) {
  document.write(`o Segundo numero é maior`);
} else {
  n1 == n2;
  document.write(`os numeros tem valores iguais`);
}
